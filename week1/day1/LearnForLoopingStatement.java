package week1.day1;

public class LearnForLoopingStatement {

	public static void main(String[] args) {

		// to run the same code multiple time use for loop
		
		// initialize ; condition ; increment / decrement
		
		for(int i = 15; i >= 1; i--) {
			System.out.println(i);
		}
		

		

	}

}
